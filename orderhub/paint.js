// BS 4800 → hex. Left intentionally sparse: without confident sRGB values we
// show these as a neutral swatch + label rather than a wrong colour. Fill in
// exact hexes here (keyed by the code with no spaces, e.g. "14C39") to enable
// real swatches for BS colours.
const BS4800 = {};

function resolvePaint(desc) {
  if (desc == null) return null;
  const s = String(desc).trim();
  if (!s) return null;

  let m = s.match(/\bRAL\s*(\d{4})\b/i);
  if (m) {
    const code = m[1];
    const info = RAL[code];
    return {
      code: `RAL ${code}`,
      label: info ? `RAL ${code} · ${info.name}` : `RAL ${code}`,
      hex: info ? info.hex : null,
    };
  }

  m = s.match(/\bBS\s*(\d{2})\s*([A-Z])\s*(\d{2})\b/i);
  if (m) {
    const code = (m[1] + m[2] + m[3]).toUpperCase();
    return { code: `BS ${code}`, label: `BS ${code}`, hex: BS4800[code] || null };
  }

  // Unrecognised format — show it as-is with no (guessed) swatch colour.
  return { code: s, label: s, hex: null };
}

module.exports = { resolvePaint };
